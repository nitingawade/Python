
#If else example
x = 10

if x > 10:
    print("x is greater than 10")
elif x < 10:
    print("x is less than 10")
else:
    print("x is equal to 10")



#Nested Loop example:

x = 10
y = 5

if x > y:
    print("x is greater than y")
    if x > 0:
        print("x is positive")
    else:
        print("x is negative")
else:
    print("x is less than or equal to y")
    if x == 0:
        print("x is zero")
    else:
        print("x is negative")

