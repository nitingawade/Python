# Example 1: Iterating over a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

print("-------------------------------------------")
# Example 2: Iterating over a string
for char in "hello":
    print(char)

print("-------------------------------------------")
# Example 3: Iterating over a range of numbers
for num in range(10):
    print(num)

print("-------------------------------------------")
# Example 3: Iterating Dict(Jsons)
emp = {"Id": "1","Name": "John"}
for em in emp:
    print(emp[em])